#右手法
dir =[[1, 0], [0, 1], [-1, 0], [0, -1]]

x, y, depth, d = 1, 1, 0, 0 #(x, y, 移動回数, 方向)

while maze[x][y] != 1:
    maze[x][y] = 2
    
    for i in range(len(dir)):
        j = (d + i -1) % len(dir)
        if maze[x + dir[j][0]][y + dir[j][1]] < 2:
            x += dir[j][0]
            y += dir[j][1]
            d = j
            depth += 1
            break
        elif maze[x + dir[j][0]][y + dir[j][1]] == 2:
            x += dir[j][0]
            y += dir[j][1]
            d = j
            depth -= 1
            break

print(depth)